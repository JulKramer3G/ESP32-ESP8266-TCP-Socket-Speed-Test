void parallelTest() {
  while (true) {
    Serial.println("Welcome to this test, please use the python script now.");
    // get number of concurrent channels from client
    bool userHasSetNumberOfChannels = false;
    int connectionCount = 0;
    while (userHasSetNumberOfChannels == false) {
      WiFiServer *managementServer = new WiFiServer(MANAGEMENT_PORT);
      managementServer->begin();

      while (userHasSetNumberOfChannels == false) {
        WiFiClient client = managementServer->available();

        if (client) {
          Serial.printf("Management client connected, system time: %d\n",
                        millis());
          delay(100);
          while (client.connected()) {
            connectionCount = client.read();
            if (connectionCount > 0) {
              Serial.printf("Client has set channel count: %d\n",
                            connectionCount);
              userHasSetNumberOfChannels = true;
              break;
            } else {
              // Serial.printf("Waiting for channel count, received: %d\n",
              //               connectionCount);
              delay(500);
            }
          }

          client.stop();
          Serial.println("Client disconnected");
        }
      }
      managementServer->close();
      managementServer->end();
      delete (managementServer);
      Serial.println("Stopped server");
    }

    Serial.println("Creating connections");
    std::list<WiFiServer *> servers;

    for (uint8_t i = 0; i < connectionCount; i += 1) {
      WiFiServer *newServer = new WiFiServer(BASE_PORT + i);
      newServer->begin();
      servers.push_back(newServer);
    }

    std::list<WiFiServer *> alreadySeenConnected;
    std::list<WiFiClient> myClients;

    Serial.println("Waiting for clients");

    bool testActive = true;

    while (testActive) {
      bool allClientsConnected = true;
      uint8_t count = 0;
      for (WiFiServer *serv : servers) {
        if (std::find(alreadySeenConnected.begin(), alreadySeenConnected.end(),
                      serv) != alreadySeenConnected.end()) {
          count += 1;
          continue;
        }
        WiFiClient client = serv->available();
        if (client) {
          if (client.connected() == true) {
            Serial.printf("Client no. %d connected\n", count);
            myClients.push_back(client);
            alreadySeenConnected.push_back(serv);
          } else {
            allClientsConnected = false;
          }
        } else {
          allClientsConnected = false;
        }
        count += 1;
      }

      if (allClientsConnected) {
        Serial.printf("Clients connected, system time: %d\n", millis());
        while (allClientsConnected) {
          uint8_t connectionCount = 0;
          for (WiFiClient client : myClients) {
            if (client) {
              if (client.connected()) {
                // Serial.printf("Sending array on connection no. %d,
                // connections " "total: %d\n", connectionCount,
                // myClients.size());
                client.write(testArray, ARRAY_SIZE);
              } else {
                Serial.println("Client is not connected anymore");
                client.stop();
                allClientsConnected = false;
                break;
              }
            } else {
              Serial.println("Client object is null");
              allClientsConnected = false;
              break;
            }
            connectionCount += 1;
          }
        }
        for (WiFiClient client : myClients) {
          client.stop();
        }
        alreadySeenConnected.clear();
        myClients.clear();
        Serial.println("Client disconnected");
        testActive = false;
      }
    }
    for (WiFiServer *serv : servers) {
      serv->close();
      serv->end();
      delete (serv);
    }
  }
}

